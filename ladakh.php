                    <p class="justify">Ladakh has a dry, chilly climate. Roughly 3 inches (80 mm) of precipitation falls on average each year; fine, dry, flaking snow is common and can occasionally fall in large amounts. The only places where vegetation grows are valleys and protected areas. Here, tamarisk (genus Tamarix) shrubs, furze (also known as gorse; spiky plants of the legume family), and other plants grow slowly and provide much-needed firewood. Wheat, barley, millet, buckwheat, peas, beans, and turnips are the main produce. The main products are textiles, namely woolen cloth.</p>
                    <p class="justify">Since the breakup of British India in 1947, India and Pakistan have been at odds over Ladakh; with the 1949 cease-fire agreement, the southeast of the region was given to India and the remaining half to Pakistan. When Chinese forces arrived in the area in the early 1960s, they took control of the part of Ladakh that was theirs.</p>
