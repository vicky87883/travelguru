                    <p class=" justify">Ladakh is a sizable portion of the northwest Indian subcontinent's Kashmir region, both east and north. Administratively, Ladakh is split between China, which also controls parts of northeastern Ladakh, and Pakistan (northwest), which is part of Gilgit-Baltistan, and India (southeast), which is part of Ladakh union territory (until October 31, 2019, part of Jammu & Kashmir state).</p>

                    

                    <p class="justify">The upper Indus River valley and the Ladakh Range, a southeastern extension of the Karakoram Range, are located in Ladakh, which has an approximate land area of 45,000 square miles (117,000 square km). One of the world's highest places is Ladakh. Deep valleys and elevated plains make up the majority of its natural characteristics. The east is dominated by the high plain, which gradually gets smaller toward the west. Rupshu is a region of big, brackish lakes in southeast Ladakh that is uniformly elevated at roughly 13,500 feet (4,100 meters). </p> 
                    <p class="justify">The Zaskar Range is located northwest of Rupshu. It is an inhospitable area where people and cattle spend most of the year indoors due to the cold. The Zaskar River drains Zaskar and flows north to join the Indus River below Leh. In the northern part of Ladakh, farmers in valley settlements at an elevation of between 2,750 and 4,550 meters (9,000 to 15,000 feet) engage in irrigation and manuring agriculture. In the highland valleys that are too high for farming, shepherds look after their flocks. The most accessible town in Ladakh, Leh, is situated 160 miles (260 km) east of Srinagar and is a major trading hub.</p>
