<p class=justify>The city is renowned for its aesthetic appeal and for having distinctive straight-line planning. Because most of its buildings are rose-colored, it is occasionally referred to as the "pink city." The main structures are the City Palace, which houses the Jaipur royal family; Jantar Mantar, an outdoor observatory from the 18th century that was inducted into the UNESCO World Heritage list in 2010; Hawa Mahal, well known as the Hall of Winds; Ram Bagh Palace; and Nahargarh, often known as the Tiger Fort. There are also libraries and museums among the public structures. The University of Rajasthan was established in 1947 and is headquartered in Jaipur.</p>

<p class=justify>Fertile alluvial plains to the east and south, and hill ranges and desert regions to the north and west, encircle the city. The main crops farmed in the area are cotton, pulses, barley, gram (chickpeas), and bajra (pearl millet). Deposits of garnet, marble, copper, mica, feldspar, iron ore, and beryllium are worked. Sariska National Park to the northeast is one of the neighboring attractions in addition to Amer. Population: 2,322,575 in 2001; 3,046,163 in 2011.</p>
