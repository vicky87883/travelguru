<p class=justify>The capital of the northwest Indian state of Rajasthan is Jaipur. It is located in the state's east-central region, about halfway between Ajmer, which is to the southwest, and Alwar, which is to the northeast. It is the most populated city in Rajasthan.</p>
<p class=justify>Built in 1727 by Maharaja Sawai Jai Singh, the walled town with a hilly backdrop to the south was intended to replace the adjacent Amber (now Amer, a suburb of Jaipur) as the capital of the princely kingdom of Jaipur, which had been established in the 12th century CE by the Rajputs. In the late 20th and early 21st centuries, Jaipur experienced tremendous expansion, with its population tripling between 1991 and 2011. The population is a mix of Muslims and Hindus. Early in the twenty-first century, the city was the subject of multiple bombing attacks, with mosques and Hindu temples falling victim.
</p>
<p class=justify>Jaipur is a well-liked travel destination with excellent air, train, and road links as well as a commercial trade hub. Manufacturing of glass, hosiery, carpets, blankets, shoes, engineering and metalworking, handloom weaving, and distilling are among the industries. Jaipur is well-known for its stone, marble, and ivory carvings in addition to its jewelry, enamel, metalwork, and printed textiles.</p>
