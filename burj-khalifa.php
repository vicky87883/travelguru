<p class=justify>In accordance with all three of the primary standards used to evaluate such structures, the Burj Khalifa, a mixed-use skyscraper located in Dubai, United Arab Emirates, is the tallest structure in the world (see to Researcher's Note: Heights of Buildings). Originally dubbed Burj Dubai while under construction, Burj Khalifa (also known as "Khalifa Tower") was formally named in honor of Sheikh Khalifa ibn Zayed Al Nahyan, the ruler of the neighboring emirate of Abu Dhabi.</p>
<p class="justify">The interior of the tower was not finished when it was officially launched on January 4, 2010. Constructed to accommodate an assortment of business, residential, and hospitality endeavors, the tower—whose precise height remained a tightly kept secret during its building—was completed at 163 stories and 2,717 feet (828 meters) in the air. Skidmore, Owings & Merrill, a Chicago-based architectural firm, created the design. The structural engineer was William F. Baker, and the architect was Adrian Smith.</p>
<p class="justify">The three-lobed footprint of the modular building is an abstract representation of the native Hymenocallis flower. One of the main factors in lowering the wind forces on the tower is the Y-shaped layout. A set of wings, each having its own concrete core and perimeter columns, support a central hexagonal core. The tower's wings step back in a spiral pattern as it rises in height, altering the structure at each level and lessening the impact of the wind on it. At the summit of the tower, the central core emerges and is completed by a spire that soars more than 700 feet (200 meters). 
</p>
<p class="justify">A hydraulic pump was used to raise the spire to its ultimate position after it was built inside the tower. The tower is supported at the base by a reinforced concrete mat that is about 13 feet (4 meters) thick and is held up by concrete piles that are 5 feet (1.5 meters) in diameter. The skyscraper is secured in place by a three-story podium, which is about 2,000,000 square feet (186,000 square meters) in size when combined with the two-story basement. More than 28,000 hand-cut glass panels, vertical stainless-steel tubular fins, and aluminum and stainless-steel panels make up the tower's external covering. The 124th level is home to the "At the Top" public observation deck.</p>
<p class="justify">When the Burj Khalifa opened in January 2010, it easily overtook Taipei 101 (Taipei Financial Center), a building located in Taipei, Taiwan, which stood at 1,667 feet (508 meters) as the highest structure in the world. The world's tallest freestanding building, the highest occupied floor, and the highest outdoor observation deck were all broken by the Burj Khalifa at the same time.
</p>
