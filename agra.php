<p class=justify>Agra is a city in the northern Indian state of Uttar Pradesh. Situated on the Yamuna (Jumna) River in the Indo-Gangetic Plain, it is roughly 200 kilometers (125 miles) southeast of Delhi.
</p>
<p class=justify>The ancient Sanskrit epic Mahabharata made reference to a "Agravana" early on, and Ptolemy is supposed to have named the location "Agra." Early in the 16th century, Sultan Sikandar of the Lodī dynasty established the city as the capital of the Delhi sultanate. During several eras of the Mughal empire, Agra also functioned as the capital. The city was overrun in the late eighteenth century by the Jats, Marathas, Mughals, the Gwalior monarch, and, in 1803, the British. From 1833 until 1868, it served as the provincial capital of Agra (after known as North-Western), and it was also one of the primary hubs of the Indian Mutiny (1857–58).</p>
<p class="justify">The 17th-century Taj Mahal, which was named a UNESCO World Heritage monument in 1983, is the main reason for visiting Agra. Often regarded as the most exquisite example of Mughal architecture in the world, the Taj Mahal is a sophisticated mausoleum. It was constructed in the middle of the 17th century by the Mughal emperor Shah Jahān for Mumtāz Maḥal, his favorite wife. The emperor Akbar erected the Agra Fort (16th century), also known as the Red Fort because to its enormous red sandstone walls. It houses the Jahangiri Mahal palace and the Pearl Mosque (Moti Masjid; 17th century), which is made of white marble. In 1983, the fort was added to the list of World Heritage sites.</p>
<p class="justify">Near the Taj Mahal are the Jāmiʿ Masjid, often known as the Great Mosque, and the exquisite white marble tomb of Iſtimād al-Dawlah (1628). Akbar's tomb is located in Sikandra, to the northwest.

Renowned for its handwoven carpets, cut stone, and leather items, Agra is a commercial and industrial hub with several road and rail intersections. One important aspect of the city's economy is tourism. Dr. B.R. Ambedkar University (previously Agra University), established in 1927, has its headquarters in this city. The state psychiatric hospital and Dayalbagh, a colony of the Radha Soami Satsang religious group (established in the city in 1861) are located in Agra's suburbs.
</p>
<p class="justify">
In Agra, there are numerous religious and cultural celebrations. The celebration of Janamashtami honors Lord Krishna's birthday. Every year in February, Shilpagram, a crafts village near the Taj Mahal, hosts the 10-day Taj Mahotsav, an arts, crafts, and music festival.
</p>
<p class="justify">The region around Agra consists almost entirely of a level plain, with hills in the extreme southwest. The region is watered by the Yamuna River and the Agra Canal. Millet, barley, wheat, and cotton are among the crops grown. The deserted Mughal city of Fatehpur Sikri is about 25 miles (40 km) southwest of Agra city. Pop. (2001) city, 1,275,134; urban agglom., 1,331,339; (2011) city, 1,585,704; urban agglom., 1,760,285.</p>
